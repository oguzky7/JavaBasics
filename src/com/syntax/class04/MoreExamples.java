package com.syntax.class04;

public class MoreExamples {

	public static void main(String[] args) {

		System.out.println("");

		boolean mothersDay = true;

		// if(mothersDay==true) { // it could be like that too but since we have
		// boolean, not necessary
		if (mothersDay) {
			System.out.println("Happy Mother's day to all Moms");
		} else {
			System.out.println("Hello");}
		System.out.println("--------------------------------");

		boolean rain = false;
		if (rain) {
			System.out.println("I'll take an umbrella");
		} else {
			System.out.println("No need for umbrella");
		}
		System.out.println("------------------------");

		boolean breakTime = true;
		if (breakTime) {
			System.out.println("Let's take a break");
		} else {
			System.out.println("Let's continue the class");
		}
	}
}
