package com.syntax.class05;

public class LogicalOr {

	public static void main(String[] args) {
		/* 
		 * OR ||
		 * true || true		--> true
		 * true || false	--> true
		 * false || true	--> true
		 * false || false	--> false
		 */
		
		String day = "Monday";

		if (day.equals("Monday") || day.equals("Friday")) {
			System.out.println();
		}
		
		
		
	}
}
