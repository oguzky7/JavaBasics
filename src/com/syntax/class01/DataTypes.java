package com.syntax.class01;

public class DataTypes {
	
	public static void main(String[] args) {
		//to create a variable: Datatypes and name
		//Whole Numbers:
		
		byte  	box1=21;
		short 	box2=2022;
		int   	box3=76666; //Generally, we are going to use this
		long  	box4=45678994613166L;
		//Decimal Numbers
		float  	container1=1.99F; //Lower case "f" also possible to use
		double 	container2=89.99;
		//To represent 1 character
		char 	gender='m';
		char 	dollar='$';
		//to represent true or false (yes or no)
		boolean hungry=true;
		boolean sleepy=false;
		
		//how to access values from variables
		
		System.out.println(container2); //Don't use quotation marks("") to access variables
		System.out.println(container1);
		System.out.println(box1);
		System.out.println(box2);
		System.out.println(box3);
		System.out.println(box4);
		System.out.println(gender);
		System.out.println(dollar);
		System.out.println(hungry);
		System.out.println(sleepy);
											//TASK!!
		// access and print values from all variables
	}
}
